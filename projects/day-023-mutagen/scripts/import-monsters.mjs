#!/usr/bin/env node
// Import monster sprite PNGs from the artworks archive into this project and
// emit a typed atlas module. Run with: `npm run import-monsters`.
//
// Source:  /home/rxtxr/projects/artworks/archive/monsters/
//   <hash>.png   — full-body sprite (1024×1024, white background)
//   <hash>.json  — sidecar metadata (mutations: [{id, level}, ...])
//
// We read sidecars directly — the archive's index.json is a stale cache.
// Sidecars always reflect the truth of what was generated.
//
// Output:
//   src/assets/monsters/<hash>.png     — downscaled to 400×400
//   src/data/monsterAtlas.ts           — ES module with typed lookups
//     - lookupAtlas(id, level)         for single-mutation PNGs
//     - lookupMulti(mutations)         for archetype/combo PNGs

import { execFileSync } from 'node:child_process';
import { copyFileSync, existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE = '/home/rxtxr/projects/artworks/archive/monsters';
const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT = resolve(__dirname, '..');
const ASSETS_DIR = resolve(PROJECT, 'src/assets/monsters');
const ATLAS_TS = resolve(PROJECT, 'src/data/monsterAtlas.ts');

if (!existsSync(SOURCE)) {
  console.error(`error: ${SOURCE} not found`);
  process.exit(1);
}

// Game renders sprites at 200×200. Source PNGs are 1024×1024. Downscale to
// 400×400 (2× for retina) — cuts bundle size ~80% with no perceptible loss.
const TARGET_SIZE = 400;
const JPEG_QUALITY = 85;

function hasMagick() {
  try { execFileSync('magick', ['--version'], { stdio: 'ignore' }); return true; }
  catch { return false; }
}
const resizeAvailable = hasMagick();
if (!resizeAvailable) {
  console.warn('warning: `magick` not found — copying PNGs at full 1024× size');
}

mkdirSync(ASSETS_DIR, { recursive: true });

// --- Read all sidecars ---
const sidecarFiles = readdirSync(SOURCE).filter((f) => f.endsWith('.json') && f !== 'index.json');
const records = [];
for (const f of sidecarFiles) {
  const meta = JSON.parse(readFileSync(join(SOURCE, f), 'utf8'));
  if (!meta.hash || !Array.isArray(meta.mutations) || meta.mutations.length === 0) continue;
  const pngPath = join(SOURCE, `${meta.hash}.png`);
  if (!existsSync(pngPath)) continue;
  records.push({
    hash: meta.hash,
    mutations: meta.mutations.map((m) => ({ id: m.id, level: Number(m.level) })),
    created: meta.created ?? '',
  });
}
// Later-generated hash wins if two sidecars map to the same state key.
records.sort((a, b) => (a.created < b.created ? -1 : 1));
console.log(`scanned ${records.length} sidecar records`);

// --- Build indices ---
function stateKey(mutations) {
  // Sort mutations by id so the key is order-independent.
  return [...mutations]
    .sort((a, b) => a.id.localeCompare(b.id))
    .map((m) => `${m.id}:${m.level}`)
    .join('|');
}

const byState = {};   // "id:level" → record (single-mutation only)
const byMulti = {};   // "id1:l1|id2:l2|..." → record (any cardinality incl. single)

for (const r of records) {
  const key = stateKey(r.mutations);
  byMulti[key] = r;
  if (r.mutations.length === 1) {
    byState[`${r.mutations[0].id}:${r.mutations[0].level}`] = r;
  }
}

// --- Copy referenced PNGs ---
const referencedHashes = new Set(records.map((r) => r.hash));
const copied = new Set();
for (const hash of referencedHashes) {
  const src = join(SOURCE, `${hash}.png`);
  const dst = join(ASSETS_DIR, `${hash}.png`);
  if (resizeAvailable) {
    execFileSync('magick', [
      src,
      '-resize', `${TARGET_SIZE}x${TARGET_SIZE}`,
      '-quality', String(JPEG_QUALITY),
      '-strip',
      dst,
    ]);
  } else {
    copyFileSync(src, dst);
  }
  copied.add(hash);
}
console.log(`${resizeAvailable ? 'resized' : 'copied'} ${copied.size} PNGs into src/assets/monsters/`);

// --- Emit TS module ---
const singleLines = Object.entries(byState)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([state, r]) => {
    const [id, level] = state.split(':');
    return `  '${state}': { id: '${id}', level: ${level}, hash: '${r.hash}' },`;
  });

const multiLines = Object.entries(byMulti)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([key, r]) => {
    const muts = r.mutations
      .map((m) => `{ id: '${m.id}', level: ${m.level} }`)
      .join(', ');
    return `  '${key}': { hash: '${r.hash}', mutations: [${muts}] },`;
  });

const ts = `// AUTO-GENERATED by scripts/import-monsters.mjs. Do not edit by hand.
// Source: ${SOURCE}/
// Entries: ${records.length} sidecars → ${Object.keys(byState).length} single-mutation, ${Object.keys(byMulti).length} multi-mutation
// Generated: ${new Date().toISOString()}

// Eager glob: Vite inlines every PNG's final URL at build time. In dev, the
// dev server serves them from src/assets/monsters/. In prod, they're bundled
// with content hashes under /assets/.
const urls = import.meta.glob('../assets/monsters/*.png', {
  eager: true,
  query: '?url',
  import: 'default',
}) as Record<string, string>;

function resolveUrl(hash: string): string | undefined {
  return urls[\`../assets/monsters/\${hash}.png\`];
}

export interface AtlasEntry {
  id: string;
  level: number;
  url: string;
}

export interface MultiAtlasEntry {
  mutations: { id: string; level: number }[];
  url: string;
  /** Score vs a query set — lower is closer. Filled in by lookupMulti. */
  score?: number;
}

interface RawSingle { id: string; level: number; hash: string }
interface RawMulti  { hash: string; mutations: { id: string; level: number }[] }

const BY_STATE: Record<string, RawSingle> = {
${singleLines.join('\n')}
};

const BY_MULTI: Record<string, RawMulti> = {
${multiLines.join('\n')}
};

/**
 * Single-mutation lookup with level fallback.
 * Tries exact level first, then nearby levels (L2 → L1/L3, etc.).
 */
export function lookupAtlas(id: string, level: number): AtlasEntry | null {
  const tryLevels = [level, level - 1, level + 1, level - 2, level + 2];
  for (const l of tryLevels) {
    if (l < 1 || l > 5) continue;
    const raw = BY_STATE[\`\${id}:\${l}\`];
    if (!raw) continue;
    const url = resolveUrl(raw.hash);
    if (url) return { id: raw.id, level: raw.level, url };
  }
  return null;
}

/**
 * Multi-mutation lookup.
 *   - exact set+level match wins outright
 *   - else: same id-set, levels nearby → distance-scored
 * Returns null if no entry has the same set of mutation ids.
 */
export function lookupMulti(
  mutations: { id: string; level: number }[],
): MultiAtlasEntry | null {
  if (mutations.length === 0) return null;
  const queryIds = [...mutations].map((m) => m.id).sort().join('|');
  const queryByLevel = new Map(mutations.map((m) => [m.id, m.level]));

  let best: { entry: RawMulti; score: number } | null = null;
  for (const entry of Object.values(BY_MULTI)) {
    if (entry.mutations.length !== mutations.length) continue;
    const entryIds = [...entry.mutations].map((m) => m.id).sort().join('|');
    if (entryIds !== queryIds) continue;

    let score = 0;
    for (const em of entry.mutations) {
      const q = queryByLevel.get(em.id);
      if (q === undefined) { score = Number.POSITIVE_INFINITY; break; }
      score += Math.abs(q - em.level);
    }
    if (best === null || score < best.score) best = { entry, score };
  }
  if (!best) return null;
  const url = resolveUrl(best.entry.hash);
  if (!url) return null;
  return {
    mutations: best.entry.mutations,
    url,
    score: best.score,
  };
}

export const ATLAS_STATES = Object.keys(BY_STATE);
export const ATLAS_MULTI_KEYS = Object.keys(BY_MULTI);
`;

writeFileSync(ATLAS_TS, ts);
console.log(`emitted ${ATLAS_TS}`);
console.log(`  single-mutation entries: ${Object.keys(byState).length}`);
console.log(`  multi-mutation entries:  ${Object.keys(byMulti).length}`);
